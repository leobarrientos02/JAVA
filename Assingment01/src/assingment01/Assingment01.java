public class Assingment01 {

    public static void main(String[] args) {
       
        // For loop to print all numbers from 0 to 100
        for( int i=0; i<=100 ; i++ ){
            // Using an If Statement to only print even numbers
            if ( i % 2 == 0 ){ 
                // This line of code prints to the screen
                System.out.println(i);
            } // Closes if Statement
        } //  Closes the For Loop
    }
} 
